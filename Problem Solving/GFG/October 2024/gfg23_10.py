class Solution:
    def sumOfLastN_Nodes(self, head, n):
        #function should return sum of last n nodes
        head1=head
        sum1 =0
        sum2 =0
        no=0
        while head:
            no+=1
            sum1 +=head.data
            head=head.next
        if n==1:return head1.data
        
        while no-n:
            sum2+=head1.data
            head1=head1.next
            no-=1
        return sum1 -sum2


